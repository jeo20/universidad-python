# Variables en Python

# Declaracion e inicializacion de variables
edad = 46
altura = 1.85
pais = 'Argentina'

# Acceder a las variables
print("Valores Iniciales: ")
print('Edad:', edad)
print('Altura:', altura)
print('Pais:', pais)

# Modificar variable
edad = 30
altura = 1.60

# Acceder a las variables
print("Valores modificados")
print('Edad:', edad)
print('Altura:', altura)
print('Pais:', pais)

# los tipos de datos son dinamicos en python
edad = 'treinta'
print('Edad:', edad)

# si queremos acceder a una variable no declarada nos sale error
telefono = 123456
print('Telefono:', telefono)